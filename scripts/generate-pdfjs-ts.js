/**
 * generate-pdfjs-ts.js
 *
 * Reads pdf.min.js and pdf.worker.min.js from node_modules/pdfjs-dist/build/
 * and writes two TypeScript files that export the content as string constants.
 *
 * These TS files are imported by PdfLoader.ts. Rollup bundles the strings
 * directly into ViewerPdf.js — so PDF.js is always available with NO external
 * CDN, NO extra files, NO deployment tricks. Works offline from day 1.
 *
 * Run automatically before every build via package.json "prebuild" script.
 */

const fs   = require("fs");
const path = require("path");

const ROOT   = path.join(__dirname, "..");
const SRC    = path.join(ROOT, "node_modules", "pdfjs-dist", "build");
const DST    = path.join(ROOT, "src", "components");

const FILES = [
    { src: "pdf.min.js",        dst: "pdfjs-lib-content.ts",    exportName: "PDFJS_LIB_CONTENT"    },
    { src: "pdf.worker.min.js", dst: "pdfjs-worker-content.ts", exportName: "PDFJS_WORKER_CONTENT" },
];

function run() {
    if (!fs.existsSync(SRC)) {
        console.error("[generate-pdfjs-ts] ERROR: pdfjs-dist not found. Run: npm install");
        process.exit(1);
    }

    for (const { src, dst, exportName } of FILES) {
        const srcPath = path.join(SRC, src);
        const dstPath = path.join(DST, dst);

        if (!fs.existsSync(srcPath)) {
            console.error(`[generate-pdfjs-ts] ERROR: not found: ${srcPath}`);
            process.exit(1);
        }

        const content = fs.readFileSync(srcPath, "utf8");
        const kb      = Math.round(content.length / 1024);

        // JSON.stringify safely escapes the content as a valid JS string literal
        const ts = [
            `// AUTO-GENERATED by scripts/generate-pdfjs-ts.js — DO NOT EDIT MANUALLY`,
            `// Source: node_modules/pdfjs-dist/build/${src} (${kb} KB)`,
            `// This file is bundled into ViewerPdf.js by Rollup at build time.`,
            `/* eslint-disable */`,
            `// @ts-nocheck`,
            `export const ${exportName}: string = ${JSON.stringify(content)};`,
            ``
        ].join("\n");

        fs.writeFileSync(dstPath, ts, "utf8");
        console.log(`[generate-pdfjs-ts] ✓  ${src}  (${kb} KB)  →  src/components/${dst}`);
    }

    console.log("[generate-pdfjs-ts] PDF.js content embedded into widget source.\n");
}

run();
